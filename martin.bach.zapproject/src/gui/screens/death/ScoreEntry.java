package gui.screens.death;

public class ScoreEntry {

	private String name = "Jonny";
	private int score = 21;

	public ScoreEntry(String name, int score) {
		super();
		this.name = name;
		this.score = score;
	}

	public String getName() {
		return name;
	}

	public int getScore() {
		return score;
	}

}
